-- Table structure for table `publisher`
create TABLE tbl_publisher(
publisher_publisherName varchar(250) PRIMARY KEY,
publisher_publisherAddress VARCHAR(250) NOT NULL ,
publisher_publisherphone varchar(50) NOT NULL
)
-- Table structure for table `book`
create table tbl_book(
book_BookID int not null GENERATED ALWAYS AS IDENTITY,
book_title varchar(250) not null,
book_publishername varchar(100) not null,
PRIMARY KEY(book_BookID),
 CONSTRAINT fk_publisher_name1 FOREIGN KEY(book_publishername) REFERENCES tbl_publisher(publisher_PublisherName) on update cascade on delete cascade
)
drop TABLE tbl_book

-- Table structure for table `branch`
create TABLE tbl_library_branch(
library_branch_BranchID int not null  GENERATED ALWAYS as identity,
library_branch_branchName varchar(100) not null,
library_branch_branchAddress varchar(200) not null,
primary key(library_branch_BranchID)
)

drop table tbl_library_branch

select * from tbl_library_branch

-- Table structure for table `borrower`
create table tbl_borrower(
borrower_cardNo int not null GENERATED by DEFAULT AS identity(start with 100 INCREMENT by 1),
borrower_BorrowerName varchar(100) not null,
borrower_BorrowerAddress varchar(100) not null,
borrower_BorrowerPhone varchar(50) not null,
primary key(borrower_cardNo))

drop table tbl_borrower

select * from tbl_borrower

-- Table structure for table `bookloan`
create table tbl_bookloan(
bookLoan_loansId int not null GENERATED by default as identity(start with 1 increment by 1),
bookLoan_bookId int not null ,
bookLoan_branchID int,
bookLoan_cardNo int not null,
bookLoan_Dateout varchar(50) not null,
bookLoan_DueDate varchar(50) not null,
constraint fk_book_id1 FOREIGN KEY(bookLoan_bookId) REFERENCES tbl_book(book_BookID) on update 	cascade on delete cascade,
constraint fk_branch_id1 foreign key(bookLoan_branchID) references tbl_library_branch(library_branch_BranchID) on update cascade on delete cascade,
constraint fk_cardNo foreign key(bookLoan_cardNo) references tbl_borrower(borrower_cardNo) on update cascade on delete cascade
)
drop table tbl_bookloan

select * from tbl_bookLoan

-- Table structure for table `bookcopies`
create table tbl_bookcopies(
bookcopies_copiesID int not null generated by default as identity(start with 1 increment by 1),
bookcopies_bookid int not null,
constraint fk_book_id2 foreign key(bookcopies_bookid) references tbl_book(book_BookID) on update cascade on delete cascade,
bookcopies_branchId int not null,
constraint fk_branch_id2 foreign key(bookcopies_branchId) references tbl_library_branch(library_branch_BranchID) on update cascade on delete cascade,
bookcopies_no_of_copies int not null,
primary key(bookcopies_copiesID)
)

drop table tbl_bookcopies

select * from tbl_bookcopies

-- Table structure for table `bookauthors`
create table tbl_bookAuthors(
bookAuthors_AuthorsId int not null generated by default as identity(start with 1 increment by 1),
bookAuthors_bookId int not null,
constraint fk_book_id3 foreign key(bookAuthors_bookId) references tbl_book(book_BookID) on update cascade on delete cascade,
bookAuthors_AuthorName varchar(50) not null,
primary key(bookAuthors_AuthorsId)
)
