/* 
1- How many copies of the book titled "The Lost Tribe" are owned by the library branch whose name is "Sharpstown"? */
select branch.library_branch_branchName as BranchName, book.book_title as books, copies.bookcopies_no_of_copies as No_of_copies
From tbl_bookcopies as copies
 inner join tbl_book as book on copies.bookcopies_bookid=book.book_bookid
 inner join tbl_library_branch as branch on branch.library_branch_branchid = copies.bookcopies_branchid
where book.book_title='The Lost Tribe'
GROUP by book.book_title

/* 
2- Retrieve the names of all borrowers who do not have any books checked out. */
select borrower_BorrowerName from tbl_borrower
where not exists
(select * from tbl_bookloan
where borrower_cardNo= bookLoan_cardNo )

/*
3- For each book that is loaned out from the "Sharpstown" branch and whose DueDate is today, retrieve the book title, the borrower's name, and the borrower's address.  */
select tbl_book.book_title as Book_title, tbl_borrower.borrower_BorrowerName as Borrower_Name, 
tbl_borrower.borrower_BorrowerAddress as Borrower_address,tbl_library_branch.library_branch_branchName as Branch_Name
From tbl_bookloan
inner join tbl_borrower on tbl_bookloan.bookLoan_cardNo =tbl_borrower.borrower_cardNo
inner join tbl_book on tbl_book.book_BookID = tbl_bookloan.bookLoan_bookId
inner join tbl_library_branch on tbl_bookloan.bookLoan_branchID=tbl_library_branch.library_branch_branchid
where tbl_library_branch.library_branch_branchName='Sharpstown' and bookLoan_DueDate='2/19/18'

/* 
4- For each library branch, retrieve the branch name and the total number of books loaned out from that branch.  */
select tbl_library_branch.library_branch_branchName as Branch_Name,count(tbl_bookloan.bookLoan_branchID) as Number_of_books
From tbl_bookloan
inner join tbl_library_branch on tbl_bookloan.bookLoan_branchID=tbl_library_branch.library_branch_branchid
Group by tbl_library_branch.library_branch_branchName
order by tbl_library_branch.library_branch_branchName

/*
5- Retrieve the names, addresses, and number of books checked out for all borrowers who have more than five books checked out. */
select tbl_borrower.borrower_borrowername as Name_,tbl_borrower.borrower_BorrowerAddress as Address, count(tbl_bookloan.bookLoan_loansId) as number_of_books
from tbl_bookloan
inner join tbl_borrower on tbl_bookloan.bookLoan_cardNo =tbl_borrower.borrower_cardNo
group by tbl_borrower.borrower_borrowername,tbl_borrower.borrower_BorrowerAddress
HAVING count(tbl_bookloan.bookLoan_loansId)> 5
order by count(tbl_bookloan.bookLoan_loansId) desc 